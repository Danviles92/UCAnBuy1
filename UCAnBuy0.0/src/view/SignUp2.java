    private void jButtonDoneActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonDoneActionPerformed
        // TODO add your handling code here:
        boolean validEmail, validPasswordLength, equalPassword;
        Util.actualUser.setEmail(jTextFieldEMail.getText());
        Util.actualUser.setPassword(jPasswordFieldPassword.getText());
        
        validEmail = sgn2c.validateEmail(Util.actualUser.getEmail());
        validPasswordLength = sgn2c.passwordLengthIsCorrect(Util.actualUser.getPassword());
        equalPassword = sgn2c.equalStrings(Util.actualUser.getPassword(), jPasswordFieldRewritePassword.getText());
        
        if(validEmail && validPasswordLength && equalPassword){
            sgn2c.saveUserInDataBase(Util.actualUser);
            Util.done = true;
            this.restoreFatherWindow();
        }
    }//GEN-LAST:event_jButtonDoneActionPerformed

    private void jTextFieldEMailKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTextFieldEMailKeyTyped
        // TODO add your handling code here:
        String email = jTextFieldEMail.getText();
        String password = jPasswordFieldPassword.getText();
        String passwordRewritten = jPasswordFieldRewritePassword.getText();
        
        if(!email.equals("") && !password.equals("") && !passwordRewritten.equals("")){
            jButtonDone.setEnabled(true);
        }else{
            jButtonDone.setEnabled(false);
        }
    }//GEN-LAST:event_jTextFieldEMailKeyTyped

