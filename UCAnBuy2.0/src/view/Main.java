    private void formWindowActivated(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowActivated
        // TODO add your handling code here:
        if(Util.ACTUAL_USER.isLogged()){
            jLabelLogIn.setText("Log Out");
            jLabelNotAMember.setText(Util.ACTUAL_USER.getUsername());
        //    log.warn("Util.ACTUAL_USER.getUsername(): " + Util.ACTUAL_USER.getUsername());
            jLabelProfile.setVisible(true);
            jLabelInventory.setVisible(true);
            jLabelSell.setVisible(true);
            jLabelSignUp.setVisible(false);
        }else{
            jLabelLogIn.setText("Log In");
            jLabelNotAMember.setText("Not a member yet?");
            jLabelProfile.setVisible(false);
            jLabelInventory.setVisible(false);
            jLabelSell.setVisible(false);
            jLabelSignUp.setVisible(true);
        }
    }//GEN-LAST:event_formWindowActivated

    private void jLabelLogInMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jLabelLogInMouseClicked
        // TODO add your handling code here:
        if(jLabelLogIn.getText().equals("Log In")){
            LogIn li = new LogIn();
            li.setVisible(true);
            li.setFatherWindow(this, true);
        }else{
            jLabelProfile.setVisible(false);
            jLabelInventory.setVisible(false);
            jLabelSell.setVisible(false);
            jLabelLogIn.setText("Log In");
            jLabelNotAMember.setText("Not a member yet?");
            jLabelSignUp.setVisible(true);
            mc.resetActualUser(Util.ACTUAL_USER);
        }
    }//GEN-LAST:event_jLabelLogInMouseClicked

