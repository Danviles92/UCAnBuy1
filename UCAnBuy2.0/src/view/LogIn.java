    private void jButtonLogInActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonLogInActionPerformed
        // TODO add your handling code here:
        String username, password;
        boolean correctUsername, correctPassword;
        
        username = jTextFieldEMail.getText();
        password = jPasswordFieldPassword.getText();
        
        correctUsername = li.correctUsername(username);
        correctPassword = li.correctPassword(username, password);
        
        if(correctUsername && correctPassword){
            Util.ACTUAL_USER = li.getUserFromDataBase(username);
            Util.ACTUAL_USER.setLogged(true);
            Util.ACTUAL_ADMIN.setUsername(Util.ACTUAL_USER.getUsername());
            this.restoreFatherWindow();
        }else{
            if(!correctUsername)
                jLabelEmailDoesNotExist.setText("E-Mail doesn't exist");
            else
                jLabelEmailDoesNotExist.setText("");
            
            if(!correctPassword)
                jLabelIncorrectPassword.setText("Incorrect Password");
            else
                jLabelIncorrectPassword.setText("");
        }
    }//GEN-LAST:event_jButtonLogInActionPerformed

