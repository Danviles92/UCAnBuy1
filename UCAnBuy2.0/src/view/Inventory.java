    public Inventory() {
        initComponents();
        this.setLocationRelativeTo(null);
        ArrayList<String> products;
        
        DefaultListModel productsList = new DefaultListModel();
        jListProductsList.setModel(productsList);
        productsList.clear();
        
        products = ic.getProductsList(Util.ACTUAL_ADMIN.getUsername());
        
        log.warn("Products: " + products);
        
        Iterator<String> productIterator = products.iterator();
        while(productIterator.hasNext()){
            String product = productIterator.next();
            
            productsList.addElement(product);
        }
    }

